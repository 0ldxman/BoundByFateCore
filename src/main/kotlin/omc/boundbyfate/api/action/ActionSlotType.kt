package omc.boundbyfate.api.action

import com.mojang.serialization.Codec

/**
 * Тип ячейки действия.
 *
 * Каждый тип имеет свой базовый кулдаун и семантику использования.
 *
 * ## Кулдауны (базовые, настраиваемые)
 *
 * - ACTION: 3-6 секунд (60-120 тиков)
 * - BONUS_ACTION: 2-3 секунды (40-60 тиков)
 * - REACTION: 1 секунда (20 тиков)
 * - LEGENDARY: настраивается индивидуально для НПС/боссов
 *
 * ## Использование
 *
 * - ACTION — основное действие: атака оружием, использование способности,
 *   использование предмета требующего действия, рывок (Dash), помощь (Help)
 *
 * - BONUS_ACTION — бонусное действие: некоторые способности классов,
 *   некоторые заклинания, Second Wind, Cunning Action (Rogue)
 *
 * - REACTION — реакция: провоцированная атака, Shield заклинание,
 *   Opportunity Attack. Игрок активирует вручную.
 *
 * - LEGENDARY — легендарное действие: только для боссов и особых НПС.
 *   Позволяет действовать в "ход" других существ.
 */
enum class ActionSlotType(
    /**
     * Базовый кулдаун в тиках.
     * Может быть переопределён через эффекты (Haste удваивает скорость).
     */
    val baseCooldownTicks: Int
) {
    /** Основное действие. Кулдаун ~4 секунды (80 тиков). */
    ACTION(80),

    /** Бонусное действие. Кулдаун ~2.5 секунды (50 тиков). */
    BONUS_ACTION(50),

    /**
     * Реакция. Кулдаун ~1 секунда (20 тиков).
     * Активируется игроком вручную, не автоматически.
     */
    REACTION(20),

    /**
     * Легендарное действие. Только для боссов/НПС.
     * Кулдаун настраивается индивидуально.
     */
    LEGENDARY(60);

    companion object {
        val CODEC: Codec<ActionSlotType> = Codec.STRING.xmap(
            { str -> valueOf(str.uppercase()) },
            { type -> type.name.lowercase() }
        )
    }
}
