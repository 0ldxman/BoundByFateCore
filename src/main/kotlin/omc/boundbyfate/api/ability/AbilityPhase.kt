package omc.boundbyfate.api.ability

/**
 * Фазы выполнения способности.
 * 
 * Способность выполняется последовательно через все фазы:
 * PREPARATION → CAST → APPLICATION → POST_APPLICATION
 */
enum class AbilityPhase {
    /**
     * Фаза подготовки (во время каста).
     * 
     * Эффекты:
     * - Визуальные эффекты подготовки
     * - Звуки каста
     * - Проверка глобальных условий
     */
    PREPARATION,
    
    /**
     * Фаза каста (момент активации).
     * 
     * Эффекты:
     * - Расход ресурсов
     * - Создание снарядов
     * - Начало концентрации
     * - Проверка материальных компонентов
     */
    CAST,
    
    /**
     * Фаза применения (попадание/воздействие).
     * 
     * Эффекты:
     * - Урон/исцеление
     * - Наложение статусов
     * - Модификация мира
     * - Спасброски
     * - Основные эффекты способности
     */
    APPLICATION,
    
    /**
     * Фаза после применения (cleanup).
     * 
     * Эффекты:
     * - Дополнительные визуальные эффекты
     * - Сообщения
     * - Условные эффекты (например, lifesteal)
     * - Cleanup
     */
    POST_APPLICATION
}
