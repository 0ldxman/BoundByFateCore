package omc.boundbyfate.api.dice

/**
 * Типы кубиков в D&D.
 * 
 * Каждый тип представляет кубик с определённым количеством граней.
 * Используется для:
 * - Бросков атаки (d20)
 * - Урона (d4, d6, d8, d10, d12)
 * - Проверок навыков (d20)
 * - Спасбросков (d20)
 * - Инициализации HP (Hit Dice)
 * - Процентных бросков (d100)
 */
enum class DiceType(val sides: Int) {
    /**
     * Четырёхгранный кубик (d4).
     * Используется для: урон кинжала, магические снаряды.
     */
    D4(4),
    
    /**
     * Шестигранный кубик (d6).
     * Используется для: урон короткого меча, Hit Dice многих классов.
     */
    D6(6),
    
    /**
     * Восьмигранный кубик (d8).
     * Используется для: урон длинного меча, Hit Dice средних классов.
     */
    D8(8),
    
    /**
     * Десятигранный кубик (d10).
     * Используется для: урон двуручного оружия, Hit Dice Fighter.
     */
    D10(10),
    
    /**
     * Двенадцатигранный кубик (d12).
     * Используется для: урон секиры, Hit Dice Barbarian.
     */
    D12(12),
    
    /**
     * Двадцатигранный кубик (d20).
     * Используется для: броски атаки, проверки навыков, спасброски.
     */
    D20(20),
    
    /**
     * Стогранный кубик (d100).
     * Используется для: процентных бросков, таблиц случайных событий.
     * Обычно реализуется как 2d10 (десятки и единицы).
     */
    D100(100);
    
    /**
     * Возвращает строковое представление кубика.
     * Например: "d20", "d6"
     */
    override fun toString(): String = "d$sides"
    
    companion object {
        /**
         * Получает тип кубика по количеству граней.
         * 
         * @param sides количество граней
         * @return тип кубика или null если не найден
         */
        fun fromSides(sides: Int): DiceType? = entries.find { it.sides == sides }
        
        /**
         * Парсит строку в тип кубика.
         * 
         * Поддерживаемые форматы:
         * - "d20", "D20"
         * - "20"
         * 
         * @param str строка для парсинга
         * @return тип кубика или null если не удалось распарсить
         */
        fun parse(str: String): DiceType? {
            val cleaned = str.trim().lowercase()
            val sides = if (cleaned.startsWith("d")) {
                cleaned.substring(1).toIntOrNull()
            } else {
                cleaned.toIntOrNull()
            }
            return sides?.let { fromSides(it) }
        }
    }
}
