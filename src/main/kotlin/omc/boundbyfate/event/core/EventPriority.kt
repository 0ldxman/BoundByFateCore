package omc.boundbyfate.event.core

/**
 * Приоритет выполнения обработчика события.
 * 
 * Обработчики выполняются в порядке от HIGHEST к LOWEST.
 * Используется для контроля порядка выполнения когда важна последовательность.
 * 
 * Примеры использования:
 * - HIGHEST: Валидация, проверка прав доступа
 * - HIGH: Изменение поведения до основной логики
 * - NORMAL: Основная логика (по умолчанию)
 * - LOW: Реакция на изменения
 * - LOWEST: Логирование, статистика
 * - MONITOR: Только наблюдение (не должен изменять данные)
 */
enum class EventPriority(val value: Int) {
    /**
     * Самый высокий приоритет.
     * Используется для критической валидации и проверок безопасности.
     */
    HIGHEST(0),
    
    /**
     * Высокий приоритет.
     * Используется для изменения поведения до основной логики.
     */
    HIGH(1),
    
    /**
     * Нормальный приоритет (по умолчанию).
     * Используется для основной бизнес-логики.
     */
    NORMAL(2),
    
    /**
     * Низкий приоритет.
     * Используется для реакции на изменения.
     */
    LOW(3),
    
    /**
     * Самый низкий приоритет.
     * Используется для логирования и статистики.
     */
    LOWEST(4),
    
    /**
     * Режим мониторинга.
     * Обработчик НЕ должен изменять данные, только наблюдать.
     * Выполняется последним, даже если событие отменено.
     */
    MONITOR(5);
    
    companion object {
        /**
         * Сортирует обработчики по приоритету.
         */
        fun sort(handlers: List<Pair<EventPriority, *>>): List<Pair<EventPriority, *>> =
            handlers.sortedBy { it.first.value }
    }
}
