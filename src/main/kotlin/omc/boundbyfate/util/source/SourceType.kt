package omc.boundbyfate.util.source

/**
 * Тип источника бонуса/особенности/модификатора.
 * 
 * Используется для отслеживания откуда пришёл эффект:
 * - Раса даёт +2 к CON
 * - Класс даёт владение оружием
 * - Фит даёт дополнительное HP
 * - Предмет даёт временный бонус к AC
 * - Заклинание даёт бафф к STR
 * - Способность даёт особенность
 * 
 * Пример:
 * ```kotlin
 * val source = SourceReference(SourceType.RACE, Identifier("boundbyfate-core", "dwarf"))
 * // Этот бонус пришёл от расы Dwarf
 * ```
 */
enum class SourceType {
    /**
     * Источник: раса персонажа.
     * Пример: Dwarf даёт +2 CON, Darkvision
     */
    RACE,
    
    /**
     * Источник: класс персонажа.
     * Пример: Fighter даёт владение всем оружием и доспехами
     */
    CLASS,
    
    /**
     * Источник: подкласс персонажа.
     * Пример: Champion даёт улучшенный критический удар
     */
    SUBCLASS,
    
    /**
     * Источник: фит (черта).
     * Пример: Tough даёт +2 HP за уровень
     */
    FEAT,
    
    /**
     * Источник: особенность (feature).
     * Пример: Unarmored Defense даёт AC = 10 + DEX + WIS
     */
    FEATURE,
    
    /**
     * Источник: экипированный предмет.
     * Пример: Belt of Giant Strength устанавливает STR = 21
     */
    ITEM,
    
    /**
     * Источник: активное заклинание.
     * Пример: Bull's Strength даёт +4 STR на 1 минуту
     */
    SPELL,
    
    /**
     * Источник: выпитое зелье.
     * Пример: Potion of Giant Strength даёт временный бонус к STR
     */
    POTION,
    
    /**
     * Источник: активная способность.
     * Пример: Rage даёт +2 к урону и сопротивление физическому урону
     */
    ABILITY,
    
    /**
     * Источник: состояние (condition).
     * Пример: Poisoned даёт помеху на броски атаки
     */
    CONDITION,
    
    /**
     * Источник: окружение.
     * Пример: Благословение святилища даёт бонус к спасброскам
     */
    ENVIRONMENTAL,
    
    /**
     * Источник: уровень персонажа.
     * Пример: Ability Score Improvement на 4 уровне
     */
    LEVEL,
    
    /**
     * Источник: административная команда.
     * Пример: /bbf stat set @s strength 20
     */
    ADMIN,
    
    /**
     * Источник: другое (для кастомных случаев).
     */
    OTHER
}

